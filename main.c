static void timer_timeout_handler(void * p_context)
{
  static int prev_humidity = 0;
  static int prev_temperature = 0;

  NRF_LOG_INFO("TIMER_TIMEOUT_HANDLER\n");

  if(humidity != prev_humidity)
  {
    humidity_characteristic_update(&m_env_sensing_service, &humidity);
  }
  prev_humidity = humidity;

  if(temperature != prev_temperature)
  {
    temperature_characteristic_update(&m_env_sensing_service, &temperature);
  }
  prev_temperature = temperature;
}

static void timer_init(void)
{
  ret_code_t err_code;
  err_code = app_timer_init();
  APP_ERROR_CHECK(err_code);

  app_timer_create(&m_timer_id, APP_TIMER_MODE_REPEATED, timer_timeout_handler);
}

void spis_event_handler(nrf_drv_spis_event_t event)
{
  if(event.evt_type == NRF_DRV_SPIS_XFER_DONE)
  {
    spis_xfer_done = true;
    humidity = (m_rx_buf[0] << 8) + m_rx_buf[1];
    temperature = (m_rx_buf[2] << 8) + m_rx_buf[3];
    NRF_LOG_INFO(" Transfer completed. Received: \n%0d\n%0d\n%0d\n%0d\n", m_rx_buf[0], m_rx_buf[1], m_rx_buf[2], m_rx_buf[3]);
    NRF_LOG_INFO("HUMIDITY = %0d", humidity);
    NRF_LOG_INFO("TEMPERATURE = %0d", temperature);
  }
}

static void spis_init(void)
{
  ret_code_t err_code;

  nrf_drv_spis_config_t spis_config = NRF_DRV_SPIS_DEFAULT_CONFIG;
  spis_config.csn_pin               = APP_SPIS_CS_PIN;
  spis_config.miso_pin              = APP_SPIS_MISO_PIN;
  spis_config.mosi_pin              = APP_SPIS_MOSI_PIN;
  spis_config.sck_pin               = APP_SPIS_SCK_PIN;

  err_code = nrf_drv_spis_init(&spis, &spis_config, spis_event_handler);
  APP_ERROR_CHECK(err_code);

  err_code = nrf_drv_spis_buffers_set(&spis, NULL, 1, m_rx_buf, m_length);
  APP_ERROR_CHECK(err_code);
}

static void gap_params_init(void)
{
    uint32_t                err_code;
    ble_gap_conn_params_t   gap_conn_params;
    ble_gap_conn_sec_mode_t sec_mode;

    BLE_GAP_CONN_SEC_MODE_SET_OPEN(&sec_mode);

    err_code = sd_ble_gap_device_name_set(&sec_mode,
                                          (const uint8_t *) DEVICE_NAME,
                                          strlen(DEVICE_NAME));
    APP_ERROR_CHECK(err_code);

    memset(&gap_conn_params, 0, sizeof(gap_conn_params));

    gap_conn_params.min_conn_interval = MIN_CONN_INTERVAL;
    gap_conn_params.max_conn_interval = MAX_CONN_INTERVAL;
    gap_conn_params.slave_latency     = SLAVE_LATENCY;
    gap_conn_params.conn_sup_timeout  = CONN_SUP_TIMEOUT;

    err_code = sd_ble_gap_ppcp_set(&gap_conn_params);
    APP_ERROR_CHECK(err_code);
}

